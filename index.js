function add(x, y){
    return x+y;
}

function subtract(x,y){
    return x- y;
}

function multiply(x,y){
    return x*y;
}

function divide(x,y){
    return x/y;
}

function increment(x){
    return ++x;
}

function decrement(x){
    return --x;
}

function makeInt(x,y=10){
    return parseInt(x,y);
}

function preserveDecimal(x){
    return parseFloat(x);
}